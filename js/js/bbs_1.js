





$(document).ready(function() {
	$("#quan").mouseover(function() {
		$(this).css({
			"background": "gainsboro",
			"color": "red"
		});
	});

	$("#quan").mouseout(function() {
		$("#quan").css({
			"background": "gainsboro",
			"color": "red"
		});
	}); 

	$("#zui").mouseover(function() {
		$("#quan").css({
			"background": "white",
			"color": "#09658e"
		});
	});
	$("#jin").mouseover(function() {
		$("#quan").css({
			"background": "white",
			"color": "#09658e"
		});
	});
	$("#re").mouseover(function() {
		$("#quan").css({
			"background": "white",
			"color": "#09658e"
		});
	});


	$("#quan").mouseover(function() {
		$("#zui").css("background", "white");

	});
	$("#quan").mouseover(function() {
		$("#jin").css("background", "white");

	});
	$("#quan").mouseover(function() {
		$("#re").css("background", "white");

	});
	
});












